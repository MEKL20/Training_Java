public class LogicTest
{
	public static void main(String [] bebas)
	{
		boolean benar=true;
		boolean salah=false;
		boolean betul=true;
		boolean meleset=false;

		System.out.println("benar && salah : "+(benar && salah));
		System.out.println("benar || salah : "+(benar || salah));
		System.out.println("benar && betul : "+(benar && betul));
		System.out.println("meleset || salah : "+(meleset || salah));
		System.out.println("!salah : "+(!salah));
		System.out.println("!benar : "+(!benar));
	}
}
